
public class Account {
	private int accNo;
	private String ownerName;
	double balance;
	
	
	public void setaccNo(int n1){
		accNo=n1;
	}
	public int getaccNo(){
	return accNo;
	}

	
	public void setownerName(String Na){
		ownerName=Na;
	}
	public String getownerName(){
	return ownerName;
	}
	
	
	public void setbalance(double b){
		balance=b;
	}
	public double getbalance(){
	return balance;
	}
}
