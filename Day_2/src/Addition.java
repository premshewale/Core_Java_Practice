
public class Addition {
	
	public void add(int n1,int n2){
	int sum=n1+n2;
	System.out.println("Addition is 2 int= "+sum);
	}
	
	public void add(float n1,float n2){
		float sum=n1+n2;
		System.out.println("Addition is 2 float= "+sum);
		}
	
	public void add(int n1,int n2,int n3){
		int sum=n1+n2+n3;
		System.out.println("Addition is 3 int= "+sum);
		}
	
	public void add(int n1,float n2){
		float sum=n1+n2;
		System.out.println("Addition is 2 int,float= "+sum);
		}
	
	public void add(String n1,String n2){
		String sum=(n1+n2);
		System.out.println("Addition is 2 String= "+sum);
		}
	
	
}
